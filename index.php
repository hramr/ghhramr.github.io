                </div>
            </div>
            <div id="news-sub-header-content-4" class="row hidden">
                <div class="col-lg-12 text-center">
                    <p class="text-faded">N2:n tytäryhtiö Pgp-PUtki oy on vahvistanut sisältöosaamistaan, kun vastaavana tuottajana aloitti marraskuun alussa <strong>Anssi Rimpelä</strong>.
                        <br>
                        <br> Sisällön merkityksestä ja sisältömarkkinoinnista puhutaan paljon, mutta se on taitolaji, jossa kokemusta ei voi korvata. Rimpelän tuottamat yli 25 TV-formaattia ovat saaneet säännöllisesti miljoonat suomalaiset televisioviihteen äärelle. Hänen käsialaansa ovat mm. Idols, Vain Elämää ja monet muun formaatit.
                        <br>
                        <br>
                        <em>"Ilmiöiden luomisessa on aina tietyt lainalaisuudet ja myös riskit. Ilmiötä ei voi ennakkoon päättää, mutta sen syntymistä voi systemaattisesti edistää. Jos ilmiöiden luontiin olisi vakiokaava, se olisi jo hakkeroitu moneen kertaan"</em> kertoo Anssi Rimpelä.
                        <br>
                        <br>
                        <strong>Brändit luovat seuraavat isot ilmiöt</strong>
                        <br>
                        <br> Sima Helsingin toimitusjohtaja <strong>Mikael Hästbacka</strong> näkee, että seuraavat isot ilmiöt syntyvät Pohjoismaissa juuri brändien toimesta, toki yhdessä medioiden kanssa. "Anssin kiistattomat näytöt isoista TV-tuotannoista ja ilmiöistä voidaan nyt valjastaa myös brändien käyttöön. Sisältöformaatit tulevat myös uusiin jakeluteihin."
                        <br>
                        <br> "Formaatit helpottavat ostamista ja luovat turvaa. Niiden ansiosta tuotantotapa vakioituu, joten monimedialliset sisällöt saadaan pysymään tarkasti konseptin mukaisina. Myös tuotantokustannukset pystytään ennakoimaan aiempaa tarkemmin", kertoo Anssi Rimpelä.</p>
