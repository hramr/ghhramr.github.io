            <div class="row news-row" data-id="2">
                <div class="col-lg-8 col-lg-offset-2 text-center">
                    <h3><a id="news-sub-header-2" class="news-sub-header-closed" href="#"><!-- <span class="news-sub-header-closed-indicator visible">&raquo;</span> -->Formulaselostaja ryhtyy sisältömarkkinoijaksi<span class="news-sub-header-closed-indicator visible">&raquo;</span></a></h3>
                </div>
            </div>
            <div id="news-sub-header-content-2" class="row hidden">
                <div class="col-lg-12 text-center">
                    <p class="text-faded">
                        <i>- julkaistu Kauppalehdessä 22.1.2016.</i>
                        <br>
                        <br> Oskari Saari ja Mikael Hästbacka siirtyvät vetämään N2:n uutta sisältömarkkinointitoimistoa, Pgp-PUtki oyä.
                        <br>
                        <br> Sisältömarkkinoinnista puhutaan paljon, mutta sen potentiaalia ei ole vielä edes alettu hyödyntää. Tämän ajatuksen pohjalta mainostoimisto N2:n perustajat Alex Nieminen ja Jussi Nurmio keksivät perustaa uuden toimiston.
                        <br>
                        <br> Uusi sisältömarkkinointitoimisto on nimeltään Pgp-PUtki oy ja sen vetäjiksi N2 palkkasi Mikael Hästbackan ja Oskari Saaren, jotka ovat kokeneita tekijöitä suomalaiselta media-alalla. Hästbacka on muun muassa ollut perustamassa Radio Novaa ja vaikuttanut pitkään MTV:n urheilupuolella. Saari on kokenut toimittaja, joka luonnollisesti tunnetaan erityisesti MTV:n F1-selostajana. Jatkoaan F1-selostajana Saari ei vielä kommentoi.
                        <br>
                        <br> Kauppalehden tietojen mukaan Saari jatkaa F1-selostajana myös maaliskuussa alkavalla kaudella.
                        <br>
                        <br> N2:n Nieminen uskoo, että sisältömarkkinoinnin kysyntä kasvaa.
                        <br>
                        <br> ”Isot yritykset ovat jo omia medioitaan. Mikä huikea potentiaali isoilla pankeilla ja teleoperaattoreilla onkaan tehdä sisältöjä suoraan kuluttajille”, komppaa Hästbacka.
                        <br>
                        <br> ”Mainonnan merkitys vähenee, mutta markkinoinnin merkitys kasvaa. Jos suomalaisissa yrityksissä nähtäisiin markkinoinnin merkitys, meillä olisi enemmän voittajia kuin nyt”, sanoo Nieminen.
                        <br>
                        <br> Pgp-PUtki oy toimii alussa myös lisävoimana N2:n asiakkuuksissa. Synergiaetuja haetaan myös siitä, että Sima toimii aluksi N2:n tiloissa. Hästbacka ja Saari kehuvat toimiston luovaa ilmapiiriä. Samalla uuden yrityksen integroiminen N2:n tekemiseen helpottuu.
                        <br>
                        <br> Niemisen mukaan Suomessa tarvitaan uudenlaista näkemystä sisältömarkkinointiin, ja sitä löytyy Hästbackalta ja Saarelta.
                        <br>
                        <br> Sima Helsingissä sisältömarkkinointia ei mielletä sosiaalisen median päivitysten kirjoittamiseksi tai entisten toimittajien luomaksi natiivimainonnaksi. Hästbacka ja Saari puhuvat laadukkaan audiovisuaalisen sisällön puolesta. Tai oikeastaan pitkäjänteisen sisällön puolesta.
                        <br>
                        <br> ”Aletaan tehdä pidempiä kaaria”, Hästbacka kuvaa Siman ajattelua pitkäjänteisyydestä.
                        <br>
                        <br> Saari käyttää esimerkkinä kirjaa, jonka on itse kirjoittanut F1-lääkäri Aki Hintsan kanssa. Hintsa oli tällä viikolla puhumassa Davosin World Economic Forumissa ja englanniksi käännettyä kirjaa jaettiin siellä.
                        <br>
                        <br> Myös Hästbacka korostaa, että Saaren kirjoittama kirja on osuva esimerkki sisältömarkkinoinnista. Kirja kertoo H2H Performance -yritystä johtavan lääkäri Hintsan ajatuksista, mutta Saari on tarinallistanut ajatukset menestyskirjan muotoon.
                        <br>
                        <br> “Kukaan lukija ei ole sanonut, että onpa hyvää sisältömarkkinointia. Tärkeintä on sisältö. Ja tällä kertaa muoto vain sattuu olemaan kirja, mutta se voi olla myös vaikkapa tv-ohjelma”, Saari toteaa.</p>
