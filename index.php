                    <p class="text-faded">Sima Helsingin vastaava tuottaja Oskari Saari on kutsuttu Suomen suurimman ja merkittävimmän digitaalisen markkinointiviestinnän töiden kilpailun, Grand Onen tuomaristoon. Saari toimii yhtenä neljästä tuomarista parhaan digitaalisen sisältömarkkinoinnin kategoriassa.
